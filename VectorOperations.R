# 2.	Find the Sum, Mean and Product of the Vector in R Programming

# Vector
num <- c(51, 16, 90, 34, 12)

# Calculate sum, mean, and product

num_sum <- sum(num)
num_mean <- mean(num)
num_product <- prod(num)

cat("The sum of elements in vector is ", num_sum, "\n")
cat("The mean of elements in vector is ", num_mean, "\n")
cat("The product of elements in vector is ", num_product, "\n")

